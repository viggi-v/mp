       
	   
	   AREA Reset, CODE,READONLY
	   EXPORT Reset_Handler

Reset_Handler
		LDR R0,N
		MOV R1,#0
		LDR R3,=0x3E8
		LDR R4,=0x00001000
		AND R2,R0,#0x0000F000
		
LOOP1000
	ADD R1,R1,R3
	SUBS R2,R2,R4
	BNE LOOP1000
	AND R2,R0,#0x00000F00
	
LOOP100
	ADD R1,R1,#0x64
	SUBS R2,R2,#0x00000100
	BNE LOOP100
	AND R2,R0,#0x000000F0
	
LOOP10
	ADD R1,R1,#0xA
	SUBS R2,R2,#0x00000010
	BNE LOOP10
	AND R2,R0,#0xF
	
LOOP1
	ADD R1,R1,#1
	SUBS R2,R2,#0x1
	BNE LOOP1
STOP B STOP

N DCD 0x3456
     END
	
	